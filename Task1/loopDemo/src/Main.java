public class Main {
    public static void main(String[] args) {

        //for döngüsü
        for (int i = 0; i<10; i++){
            System.out.println(i);
        }
        System.out.println("Döngü Bitti...");


        //while döngüsü

        int i =1;
        while (i<10){
            System.out.println(i);
            i++;
        }
        System.out.println("While döngüsü bitti...");

        //do while döngüsü

        int x = 2;
        do{
            System.out.println(x);
        }
        while(x<10);
            System.out.println("Do while döngüsü bitti...");


    }

}